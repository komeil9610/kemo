const coverageLookup = new Map(
  technicianCoverageOptions.flatMap((item) =>
    [item.key, item.ar, item.en, ...(item.aliases || [])]
      .map((alias) => String(alias || '').trim().toLowerCase())
      .filter(Boolean)
      .map((alias) => [alias, item.key])
  )
);

export const normalizeTechnicianCoverageKey = (value) => {
  const normalized = String(value || '').trim().toLowerCase();
  if (!normalized) {
    return '';
  }
  return coverageLookup.get(normalized) || normalized;
};

export const getTechnicianCoverageOption = (value) => {
  const normalized = normalizeTechnicianCoverageKey(value);
  return technicianCoverageOptions.find((item) => item.key === normalized) || null;
};

export const getTechnicianCoverageLabel = (value, lang = 'ar') => {
  const option = getTechnicianCoverageOption(value);
  if (option) {
    return lang === 'ar' ? option.ar : option.en;
  }
  return String(value || '').trim() || (lang === 'ar' ? 'غير محدد' : 'Unassigned');
};

export const getTechnicianCoverageLabels = (values = [], lang = 'ar') => {
  const items = (Array.isArray(values) ? values : [values])
    .map((item) => getTechnicianCoverageLabel(item, lang))
    .filter(Boolean);
  return items.length ? items : [lang === 'ar' ? 'غير محدد' : 'Unassigned'];
};

export const getTechnicianCoverageType = (value) => getTechnicianCoverageOption(value)?.type || 'custom';

export const doesTechnicianCoverCity = (coverageValues = [], cityValue = '') => {
  const cityKey = normalizeTechnicianCoverageKey(cityValue);
  if (!cityKey) {
    return true;
  }

  const coverageKeys = Array.isArray(coverageValues) ? coverageValues : [coverageValues];
  const normalizedCoverage = coverageKeys.map((item) => normalizeTechnicianCoverageKey(item)).filter(Boolean);

  return normalizedCoverage.some((key) => {
    if (key === cityKey) {
      return true;
    }
    const option = getTechnicianCoverageOption(key);
    if (option?.type !== 'region') {
      return false;
    }
    return (coverageRegionMembers[key] || []).includes(cityKey);
  });
};
